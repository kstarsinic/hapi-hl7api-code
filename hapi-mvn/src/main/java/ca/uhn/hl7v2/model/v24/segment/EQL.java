  /**
   * Creates a EQL (Embedded Query Language) segment object that belongs to the given 
   * message.  
   */
  public EQL(Group parent, ModelClassFactory factory) {
    super(parent, factory);
    Message message = getMessage();
    try {
       this.add(ST.class, false, 1, 32, new Object[]{message});
       this.add(ID.class, true, 1, 1, new Object[]{message, new Integer(106)});
       this.add(CE.class, true, 1, 250, new Object[]{message});
       this.add(ST.class, true, 1, 4096, new Object[]{message});
    } catch (HL7Exception he) {
        HapiLogFactory.getHapiLog(this.getClass()).error("Can't instantiate " + this.getClass().getName(), he);
    }
  }

